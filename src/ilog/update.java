    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if(jTextField2.getText().equals("") || jTextField3.getText().equals("")|| jTextField4.getText().equals("")
                || jTextField6.getText().equals("")|| jTextField7.getText().equals("")
                || jComboBox1.getSelectedItem().toString().equals("<none>") || 
                (jComboBox1.getSelectedItem().toString().equals("yes") && jComboBox2.getSelectedItem().toString().equals("<none>")))
        {
            JOptionPane.showMessageDialog(this, "Please enter all values");
            return;
        }
        int id, age;
        float salary;
        String name, paid_yn, mail, no;
        id=Integer.parseInt(getid());
        jTextField1.setEditable(false);
        name=(jTextField2.getText());
        salary=Integer.parseInt(jTextField3.getText());
        age=Integer.parseInt(jTextField4.getText());
        paid_yn=(String)jComboBox1.getSelectedItem();
        mail=jTextField6.getText();
        no=jTextField7.getText();
        BasicDBObject where = new BasicDBObject("id",id);
        BasicDBObject updateFields = new BasicDBObject();
        updateFields.append("name", name);
        updateFields.append("salary", salary);
        updateFields.append("age", age);
        updateFields.append("no", no);
        updateFields.append("mail", mail);
        updateFields.append("if_paid", paid_yn);
        updateFields.append("month",mon);
        BasicDBObject setQuery = new BasicDBObject();
        setQuery.append("$set", updateFields);
        collection.update(where, setQuery);
        JOptionPane.showMessageDialog(this, "Updated");
        this.dispose();
        new log().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

